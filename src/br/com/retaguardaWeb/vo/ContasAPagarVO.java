package br.com.retaguardaWeb.vo;


public class ContasAPagarVO {

	private String numParcela;
	private String valorParcela;
	private String dataParcela;
	private String descricao;
	public String getNumParcela() {
		return numParcela;
	}
	public void setNumParcela(String numParcela) {
		this.numParcela = numParcela;
	}
	public String getValorParcela() {
		return valorParcela;
	}
	public void setValorParcela(String valorParcela) {
		this.valorParcela = valorParcela;
	}
	public String getDataParcela() {
		return dataParcela;
	}
	public void setDataParcela(String dataParcela) {
		this.dataParcela = dataParcela;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
	
	
}
